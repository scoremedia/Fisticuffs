import UIKit


private var b_image_key = 0


public extension UIImageView {

    var b_image: BindableProperty<UIImageView, UIImage?> {
        associatedObjectProperty(self, &b_image_key) { _ in
            BindableProperty(self, setter: { control, value in
                control.image = value
            })
        }
    }

}
